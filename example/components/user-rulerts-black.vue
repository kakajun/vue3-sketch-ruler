<template>
  <div class="wrapper2">
    <SketchRule
      :eyeIcon="eyeIcon"
      :closeEyeIcon="closeEyeIcon"
      :thick="state.thick"
      :scale="state.scale2"
      :palette="state.palette"
      :width="580"
      :height="580"
      :start-x="state.startX"
      :start-y="state.startY"
      :shadow="shadow"
      :isShowReferLine="state.isShowReferLine"
      @onCornerClick="handleCornerClick"
      :lines="state.lines"
    >
    </SketchRule>
    <div
      id="screens2"
      ref="screensRef"
      @wheel="handleWheel"
      @scroll="handleScroll"
    >
      <div ref="containerRef" class="screen-container">
        <div id="canvas2" :style="canvasStyle" />
      </div>
    </div>
  </div>
</template>
<script setup lang="ts">
import eyeIcon from './eye.svg'
import closeEyeIcon from './closeEyeIcon.svg'
import { computed, ref, reactive, onMounted, nextTick } from 'vue'
import SketchRule from '../../src/index' // 这里可以换成打包后的
const rectWidth = 600
const rectHeight = 320
const screensRef = ref(null)
const containerRef = ref(null)
const state = reactive({
  scale: 0.75, //658813476562495, //1,
  startX: 0,
  startY: 0,
  lines: {
    h: [433, 588],
    v: [33, 143]
  },
  palette: {
    bgColor: '#181b24', // ruler bg color
    longfgColor: '#BABBBC', // ruler longer mark color
    shortfgColor: '#9C9C9C', // ruler shorter mark color
    fontColor: '#DEDEDE', // ruler font color
    shadowColor: '#525252', // ruler shadow color
    lineColor: '#EB5648',
    borderColor: '#B5B5B5',
    cornerActiveColor: '#fff'
  },
  thick: 20,
  isShowRuler: true, // 显示标尺
  isShowReferLine: true // 显示参考线
})
const shadow = computed(() => {
  return {
    x: 0,
    y: 0,
    width: rectWidth,
    height: rectHeight
  }
})
const canvasStyle = computed(() => {
  return {
    width: rectWidth,
    height: rectHeight,
    transform: `scale(${state.scale})`
  }
})
onMounted(() => {
  // 滚动居中
  screensRef.value.scrollLeft =
    containerRef.value.getBoundingClientRect().width / 2 - 400
})

const handleScroll = () => {
  const screensRect = document
    .querySelector('#screens2')
    .getBoundingClientRect()
  const canvasRect = document.querySelector('#canvas2').getBoundingClientRect()

  // 标尺开始的刻度
  const startX =
    (screensRect.left + state.thick - canvasRect.left) / state.scale
  const startY = (screensRect.top + state.thick - canvasRect.top) / state.scale
  state.startX = startX
  state.startY = startY
}
const handleCornerClick = e => {
  console.log('handleCornerClick', e)
}
// 控制缩放值
const handleWheel = (e: {
  ctrlKey: any
  metaKey: any
  preventDefault: () => void
  deltaY: number
}) => {
  if (e.ctrlKey || e.metaKey) {
    e.preventDefault()
    const nextScale = parseFloat(
      Math.max(0.2, state.scale - e.deltaY / 500).toFixed(2)
    )
    state.scale = nextScale
  }
  nextTick(() => {
    handleScroll()
  })
}
const showLineClick = () => {
  state.isShowReferLine = !state.isShowReferLine
  console.log(state.isShowReferLine, 'state.isShowReferLine')
}
</script>
